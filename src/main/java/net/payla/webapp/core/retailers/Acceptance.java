package net.payla.webapp.core.retailers;

public class Acceptance {
    private String acceptanceNamePersian;
    private String acceptanceNameLatin;
    private String acceptanceBio;
    private int acceptanceOrderNumber;

    public String getAcceptanceNamePersian() {
        return acceptanceNamePersian;
    }

    public void setAcceptanceNamePersian(String acceptanceNamePersian) {
        this.acceptanceNamePersian = acceptanceNamePersian;
    }

    public String getAcceptanceNameLatin() {
        return acceptanceNameLatin;
    }

    public void setAcceptanceNameLatin(String acceptanceNameLatin) {
        this.acceptanceNameLatin = acceptanceNameLatin;
    }

    public String getAcceptanceBio() {
        return acceptanceBio;
    }

    public void setAcceptanceBio(String acceptanceBio) {
        this.acceptanceBio = acceptanceBio;
    }

    public int getAcceptanceOrderNumber() {
        return acceptanceOrderNumber;
    }

    public void setAcceptanceOrderNumber(int acceptanceOrderNumber) {
        this.acceptanceOrderNumber = acceptanceOrderNumber;
    }
}
